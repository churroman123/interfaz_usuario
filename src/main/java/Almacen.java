     public void mostarProveedoresComboBox(JComboBox combobox){
        CConexion con = new CConexion();
        //se hace conexi√≥n a la base de datos y seejecuta la sentencia que esta dentro del executeQuery()
        try{
            Statement stmt = con.establecerConexion().createStatement();
            ResultSet rs = stmt.executeQuery("select nomprov from proveedores");
            
            DefaultComboBoxModel<String> modelbox = new DefaultComboBoxModel<>();
            while (rs.next()) {
                modelbox.addElement(rs.getString("nomprov"));
            }
            
            combobox.setModel(modelbox);
            
            rs.close();
            stmt.close();
            con.establecerConexion().close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"error: "+ ex);
        }
    }
